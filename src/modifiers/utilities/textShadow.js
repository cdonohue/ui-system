export default function generate(config) {
  const { textShadows = {} } = config

  return Object.keys(textShadows).reduce((classNames, key) => {
    const modifier = key === "default" ? "" : `-${key}`

    return {
      ...classNames,
      [`text-shadow${modifier}`]: [`text-shadow: ${textShadows[key]}`],
    }
  }, {})
}
